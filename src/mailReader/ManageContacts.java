    private void jButton19ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton19ActionPerformed
// TODO add your handling code here:
        boolean found = false;
        for(int i=0;i<parent.db.contacts.size();i++) {
            if(parent.db.contacts.get(i).emailID.equalsIgnoreCase(jTextID.getText())) {
                found = true;
                break;
            }
        }
        if(found) {
            new MessageBox(this,"EMail ID Already Registered!");
            return;
        }
        if(jTextID.getText().equals("")) {
            new MessageBox(parent, "Invalid ID!");
        }

        SingleContact sc = new SingleContact();
        sc.emailID = jTextID.getText();
        sc.name = jTextUserName.getText();

        try {
            sc.rsaMod = Long.parseLong(jTextModulus.getText());
            sc.rsaPublic = Long.parseLong(jTextPublicKey.getText());
        }catch(Exception e) {
            System.out.println("Exception: " + e);
        }
        
        parent.db.contacts.add(sc);
        parent.writeToFile();
        refreshList();
        
        jTextID.setText("");
        jTextModulus.setText("");
        jTextPublicKey.setText("");
        jTextUserName.setText("");
        
    }//GEN-LAST:event_jButton19ActionPerformed
    
