    private void jButton20ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton20ActionPerformed
        // TODO add your handling code here:
        int index = jListAccounts.getSelectedIndex();
        if(index==-1) {
            return;
        }
        MessageBox mb = new MessageBox(parent , "Are You Sure?");
        mb.setVisible(true);
        if(!mb.ok) {
            return;
        }
        parent.db.contacts.remove(index);
        parent.writeToFile();
        refreshList();
        
    }//GEN-LAST:event_jButton20ActionPerformed

    private void jListAccountsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListAccountsMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount()!=2) {
            return;
        }
        
        int index= jListAccounts.getSelectedIndex();
        if(index==-1) {
            return;
        }
        SingleContact sc = parent.db.contacts.get(index);
        jTextID.setText(sc.emailID);
        jTextModulus.setText("" + sc.rsaMod);
        jTextPublicKey.setText("" + sc.rsaPublic);
        jTextUserName.setText("" + sc.name);
        
    }//GEN-LAST:event_jListAccountsMouseClicked

