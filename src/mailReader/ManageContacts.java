    public void refreshList() {
        lmAccounts.clear();
        for(int i=0;i<parent.db.contacts.size();i++) {
            SingleContact sid = parent.db.contacts.get(i);
            lmAccounts.addElement(sid.name + " at " + sid.emailID);
        }
        jListAccounts.setModel(lmAccounts);
    }
    
